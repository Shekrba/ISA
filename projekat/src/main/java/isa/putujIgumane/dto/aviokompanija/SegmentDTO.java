package isa.putujIgumane.dto.aviokompanija;



public class SegmentDTO {

	
	private Integer id;
	
	private Short rbr;
	
	private Double cena;
	
	private Double x;
	
	private Double y;
	
	private Short popust;
	
	private Short redovi;
	
	private Short kolone;
	
	public SegmentDTO() {
		
	}

	
	
	public Short getPopust() {
		return popust;
	}



	public void setPopust(Short popust) {
		this.popust = popust;
	}



	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Short getRbr() {
		return rbr;
	}

	public void setRbr(Short rbr) {
		this.rbr = rbr;
	}

	public Double getCena() {
		return cena;
	}

	public void setCena(Double cena) {
		this.cena = cena;
	}

	public Double getX() {
		return x;
	}

	public void setX(Double x) {
		this.x = x;
	}

	public Double getY() {
		return y;
	}

	public void setY(Double y) {
		this.y = y;
	}

	public Short getRedovi() {
		return redovi;
	}

	public void setRedovi(Short redovi) {
		this.redovi = redovi;
	}

	public Short getKolone() {
		return kolone;
	}

	public void setKolone(Short kolone) {
		this.kolone = kolone;
	}

	
	
	
	
}
